func FindOne1(nums []int) int {
	countMap := make(map[int]int)

	for _, num := range nums {
		countMap[num]++
	}

	for key, val := range countMap {
		if val == 1 {
			return key
		}
	}
	return 0
}

func FindOne(nums []int) int {
	result := 0
	for _, num := range nums {
		temp := result
		result ^= num
		fmt.Printf("num = %d, result = %d, result ^ num = %d \n", num, temp, result)
	}
	return result
}

